        public async Task<ProductBacklogItem?> GetByIdAsync(int id)
        {
            return await _context.Stories
                .FirstOrDefaultAsync(s => s.PbiId == id);
        }

        public async Task<ProductBacklogItem> AddAsync(ProductBacklogItem story)
        {
            // Note: Id will be auto-generated by the database (identity column)
            _context.Stories.Add(story);
            await _context.SaveChangesAsync();
            return story;
        }

        public async Task<ProductBacklogItem> UpdateAsync(ProductBacklogItem story)
        {
            _context.Entry(story).State = EntityState.Modified;
            await _context.SaveChangesAsync();
            return story;
        }

        public async Task<bool> DeleteAsync(int id) //This is a full delete right now. We may want to consider a soft delete. To Be Determined - TB
        {
            var story = await _context.Stories.FindAsync(id);
            if (story == null)
            {
                return false;
            }

            _context.Stories.Remove(story);
            await _context.SaveChangesAsync();
            return true;
        }

        public async Task<IEnumerable<ProductBacklogItem>> GetByStatusAsync(PbiStatus status)
        {
            return await _context.Stories
                .Where(s => s.Status == status)
                .OrderByDescending(s => s.DateCreated)
                .ToListAsync();
        }

        public async Task<IEnumerable<ProductBacklogItem>> GetDraftPbisAsync()
        {
            return await _context.Stories
                .Where(s => s.IsDraft)
                .OrderByDescending(s => s.DateCreated)
                .ToListAsync();
        }

        public async Task<IEnumerable<ProductBacklogItem>> GetNonDraftPbisAsync()
        {
            return await _context.Stories
                .Where(s => !s.IsDraft)
                .OrderByDescending(s => s.DateCreated)
                .ToListAsync();
        }
    }
}